function winrate2(){
    let str = document.getElementById("winrate2").innerText;
    let lastCh = str.charAt(str.length - 1);
    if (lastCh != '%'){
        let wins = document.getElementById("winrate2").getAttribute("data-winrate");
        document.getElementById("winrate2").innerHTML = wins;
    }else{
        let nick = document.getElementById("winrate2").getAttribute("data-nick");
        document.getElementById("winrate2").innerHTML = nick;
    }
}

